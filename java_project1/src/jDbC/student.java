package jDbC;

public class student {
     int sid;
     String sname;
     int phy;
     int chem;
     float avg;
     
	public student(int sid, String sname, int phy, int chem, float avg) {
		super();
		this.sid = sid;
		this.sname = sname;
		this.phy = phy;
		this.chem = chem;
		this.avg = avg;
	}
}
